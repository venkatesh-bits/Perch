// EV charging across South India, organised BY DISTRICT.
//
// Honest scope: a hand-maintained file can NOT list every charger (Chennai alone
// has 130+). So this does two things:
//   1. Curated, map-placed stations we can name from public network data
//      (Tata Power, Statiq, Ather, Zeon, chargeMOD, KSEB, OnePlug, BPCL, Ola).
//   2. A DISTRICT DIRECTORY covering every district we care about - each links
//      out to the live, always-current sources (Google Maps + govt e-AMRIT map)
//      so completeness never depends on us hand-copying pins that go stale.
//
// ⚠ Always verify availability in the operator's app before relying on a stop.

export type Connector = 'CCS2' | 'CHAdeMO' | 'Type2' | 'Bharat_DC' | 'Bharat_AC'
export type ChargerSpeed = 'fast' | 'standard'
export type StationStatus = 'public' | 'guests_only' | 'hotel' | 'restaurant'

export interface EvStation {
  id: string
  name: string
  network: string
  area: string          // locality within the district
  district: string
  state: string
  lat: number
  lng: number
  connectors: Connector[]
  powerKw: number
  speed: ChargerSpeed
  status: StationStatus
  note?: string
}

// Open-data station (OSM / OCM), generated by scripts/fetch-ev-stations.ts.
// Same shape as a curated station plus provenance + nearest-hill-station linkage.
export interface OsmEvStation extends EvStation {
  source: 'osm' | 'ocm'
  nearestDestSlug: string | null
  distanceToDestKm: number | null
}

export interface DistrictEntry {
  district: string
  state: string
  blurb?: string
}

