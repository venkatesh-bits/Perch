// ─── Links to live, always-current sources (no API, just URLs) ────────────────

/** Google Maps live search for "EV charging" within a district. Opens in a new tab. */
export function districtGoogleMapsUrl(d: DistrictEntry): string {
  return `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(`EV charging stations in ${d.district}, ${d.state}`)}`
}

/** Government e-AMRIT national charging map (NITI Aayog). */
export const E_AMRIT_URL = 'https://e-amrit.niti.gov.in/charging-map'

/** Resolve a single curated station to a Google Maps place search. */
export function googleMapsUrl(s: EvStation): string {
  return `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(`${s.name}, ${s.area}, ${s.district} EV charging`)}`
}

/** Directions to a station from the user's location. */
export function googleMapsDirectionsUrl(s: EvStation): string {
  return `https://www.google.com/maps/dir/?api=1&destination=${s.lat},${s.lng}`
}

export function stationsInDistrict(district: string): EvStation[] {
  return EV_STATIONS.filter((s) => s.district === district)
}

