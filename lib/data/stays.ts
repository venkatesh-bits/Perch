export interface BookingLink {
  label: string
  url: string
  /** True if we earn affiliate revenue (or could) via this provider. */
  monetizable: boolean
}

/**
 * Legal outbound deep-links to each OTA's own search for a place. We send the
 * traveller to the provider's live, current inventory - we never copy it.
 * `place` should be a human query like "Ooty, Tamil Nadu".
 */
export function bookingSearchLinks(place: string): BookingLink[] {
  const q = encodeURIComponent(place)
  return [
    { label: 'Google Maps', url: `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent('stays in ' + place)}`, monetizable: false },
    { label: 'Airbnb', url: `https://www.airbnb.com/s/${q}/homes`, monetizable: false },
    { label: 'Booking.com', url: `https://www.booking.com/searchresults.html?ss=${q}`, monetizable: true },
    { label: 'Agoda', url: `https://www.agoda.com/search?text=${q}`, monetizable: true },
    { label: 'MakeMyTrip', url: `https://www.google.com/search?q=${encodeURIComponent(place + ' hotels')}+site%3Amakemytrip.com`, monetizable: true },
  ]
}

/** A coordinate maps link is reliable even when a stay has no good name. */
export function stayMapsLink(s: { lat: number; lng: number }): string {
  return `https://www.google.com/maps/search/?api=1&query=${s.lat},${s.lng}`
}
