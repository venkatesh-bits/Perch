// Stays (accommodation) layer - types, labels and OUTBOUND booking deep-links.
//
// Perch does NOT mirror Airbnb/OTA listings (no public API, scraping breaks their
// ToS + copyright, and the only "Airbnb APIs" are paid metered scrapers - exactly
// the surprise-billing risk we avoid). Instead:
//   1. Open-data stays from OpenStreetMap (keyless, $0) - generated into stays-osm.ts.
//   2. Community stays in Supabase (work-from-here WiFi/power ratings).
//   3. Legal deep-LINKS to each OTA's own search for a place (no data copied).
//
// Linking to a public search URL is fine; only re-hosting their listings is not.

export type StayType =
  | 'hotel' | 'homestay' | 'guest_house' | 'hostel' | 'apartment' | 'resort' | 'chalet' | 'motel'

// Open-data stay (OSM), generated by scripts/fetch-stays.ts.
export interface OsmStay {
  id: string
  name: string
  type: StayType
  area: string
  district: string
  state: string
  lat: number
  lng: number
  stars: number | null
  website: string | null
  phone: string | null
  hasInternet: boolean | null
  source: 'osm'
  nearestDestSlug: string
  distanceToDestKm: number
}

